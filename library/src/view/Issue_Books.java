    private void issueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueActionPerformed
              String BookRefNo=ref_no.getText();
              
              
              
            String ID=student_id.getText();
            String Name=student_name.getText();
            String CONTACT=student_contact.getText();
            
            
             Validations v=new Validations();
             
              
              if(v.idValidate(ID)){
                  if(v.FirstNamevalidate(Name)){
                      if(v.ContactValidate(CONTACT)){
                  
                  
         //   this.dispose();
                Librarian_Model lm=new Librarian_Model();
                
             lm.Issue(BookRefNo,ID, Name, CONTACT);
              }
                 else{
              JOptionPane.showMessageDialog(null,"Enter proper CONTACT");
                      } 
                  }
                  
              else{
              JOptionPane.showMessageDialog(null,"Enter proper Name");
              }
                 
                  
                  
              }
              else{
              JOptionPane.showMessageDialog(null,"Enter proper ID");
              }
                // TODO add your handling code here:
    }//GEN-LAST:event_issueActionPerformed

