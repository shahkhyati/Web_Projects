    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
               
                
                
                ID=id.getText();
               
          
            int PersonID = Integer.parseInt(ID);

                String FirstName=uname.getText();
                String LastName=lname.getText();
                
                String Email=email.getText();
                String Address=address.getText();
                String City=city.getText();
                String ContactNumber=num.getText();
                String Password=pass.getText();
              if(v.FirstNamevalidate(FirstName) && v.LastNamevalidate(LastName)){
                if(v.emailValidate(Email)){
               
               
                Admin_Model am=new Admin_Model();
                
                am.Insert(PersonID,FirstName, LastName, Email, Address, City,ContactNumber,Password);
        // TODO add your handling code here:
               }
               else{
               JOptionPane.showMessageDialog(null,"Enter proper email or ContactNumber");
               }
              }
              else{
              JOptionPane.showMessageDialog(null,"Enter proper FirstName or LastName");
              }
               // this.dispose();
    }//GEN-LAST:event_submitActionPerformed

