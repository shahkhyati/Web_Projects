                DefaultTableModel tm=new DefaultTableModel();
                tm.setColumnIdentifiers(columnNames);
                table.setModel(tm);
                
                String role="";
                String PersonID="";
                String FirstName="";
                String LastName="";
                String Email="";
                String Address="";
                String City="";
                String ContactNumber="";
